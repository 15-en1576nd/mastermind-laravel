    /**
     * Store a newly created resource in storage.
     *
     * @param  App\Http\Requests\StoreRegisterRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreRegisterRequest $request)
    {
        // try to create a new user
        // user create and hash password
        User::create([
            'name' => $request->validated()['name'],
            'email' => $request->validated()['email'],
            'password' => Hash::make($request->validated()['password']),
        ]);

        // if user creation was successful then attempt to log the user in
        if (auth()->attempt($request->validated())) {
            // If successful, redirect to their intended location
            return redirect()->intended('/');
        }

        // if user creation was unsuccessful then redirect back to the register page
        return redirect()->back()->withInput();
    }

